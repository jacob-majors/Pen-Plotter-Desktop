def text_to_paths(text: str, x: float, y: float, size: float = 5.0) -> list:
    """
    Convert text to a list of plotter paths.
    x, y: starting position (mm)
    size: height of characters (mm)
    """
    paths = []
    curr_x = x
    spacing = size * 0.2
    char_w = size * 0.7
    
    for char in text.upper():
        if char in STROKE_FONT:
            char_paths = STROKE_FONT[char]
            for stroke in char_paths:
                # Scale and shift
                scaled_stroke = [
                    (curr_x + (px / 10.0) * char_w,
                     y + (py / 10.0) * size)
                    for px, py in stroke
                ]
                paths.append(scaled_stroke)
        curr_x += char_w + spacing
        
    return paths
